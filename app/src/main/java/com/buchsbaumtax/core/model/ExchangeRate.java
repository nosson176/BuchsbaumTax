package com.buchsbaumtax.core.model;


public class ExchangeRate {
    private String currency;
    private String year;
    private String show;
    private double rate;

    public String getCurrency() {
        return currency;
    }

    public String getYear() {
        return year;
    }

    public String getShow() {
        return show;
    }

    public double getRate() {
        return rate;
    }
}
